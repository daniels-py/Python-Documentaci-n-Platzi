a = 10  # Asigna el valor 10 a la variable a
b = 3   # Asigna el valor 3 a la variable b

print(a > b)  # Compara si a es mayor que b, imprime True porque 10 > 3
print(a < b)  # Compara si a es menor que b, imprime False porque 10 no es < 3
print(a >= b)  # Compara si a es mayor o igual que b, imprime True porque 10 >= 3
print(a <= b)  # Compara si a es menor o igual que b, imprime False porque 10 no es <= 3
print(a == b)  # Compara si a es igual a b, imprime False porque 10 no es igual a 3
print(a != b)  # Compara si a es diferente de b, imprime True porque 10 es diferente de 3
