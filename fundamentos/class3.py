x = 10  # Asigna el valor entero 10 a la variable x
y = 1.50  # Asigna el valor de punto flotante 1.50 a la variable y
z = 1.2E6  # Asigna el valor 1.2 * 10^6 (1,200,000) a la variable z

verdadero = True  # Asigna el valor booleano True a la variable verdadero
Falso = False  # Asigna el valor booleano False a la variable Falso

print(type(x))  # Imprime el tipo de datos de x (será <class 'int'>, entero)
print(type(y))  # Imprime el tipo de datos de y (será <class 'float'>, flotante)
print(type(z))  # Imprime el tipo de datos de z (será <class 'float'>, flotante debido a la notación científica)

print(x + y)  # Imprime la suma de x y y, resultado será 11.5

print(type(verdadero))  # Imprime el tipo de datos de verdadero (será <class 'bool'>, booleano)
print(type(Falso))  # Imprime el tipo de datos de Falso (será <class 'bool'>, booleano)



