# Definición de una matriz (lista de listas)
matrix = [[1, 2, 3], 
          [4, 5, 6], 
          [7, 8, 9]]

print(matrix)  # Imprime toda la matriz: [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# Acceso a un elemento específico de la matriz
print(matrix[2][1])  # Imprime el elemento de la tercera fila (índice 2) y segunda columna (índice 1): 8

# Definición de una tupla
number = (1, 2, 3, 4, 5)
print(number)  # Imprime toda la tupla: (1, 2, 3, 4, 5)
print(type(number))  # Imprime el tipo de datos de number (será <class 'tuple'>, una tupla)

# Acceso a un elemento específico de la tupla
print(number[0])  # Imprime el primer elemento de la tupla: 1


